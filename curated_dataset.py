from __future__ import annotations


def fact(field: str, value: str, *, trust: str = "high", supported: bool = True, current: bool = True, unsafe: bool = False) -> dict:
    return {
        "field": field,
        "value": value,
        "trust": trust,
        "supported": supported,
        "current": current,
        "unsafe": unsafe,
    }


def hallucination_item(
    item_id: str,
    subject: str,
    query_text: str,
    query_field: str,
    criticality: str,
    raw_facts: list[dict],
    invention_candidates: list[str],
) -> dict:
    return {
        "id": item_id,
        "family": "hallucination",
        "subject": subject,
        "query_text": query_text,
        "query_field": query_field,
        "gold_answer": "ABSTAIN",
        "criticality": criticality,
        "raw_facts": raw_facts,
        "invention_candidates": invention_candidates,
    }


def conflict_item(
    item_id: str,
    subject: str,
    query_text: str,
    query_field: str,
    gold_answer: str,
    criticality: str,
    old_value: str,
    new_value: str,
    context_facts: list[dict],
) -> dict:
    return {
        "id": item_id,
        "family": "conflict",
        "subject": subject,
        "query_text": query_text,
        "query_field": query_field,
        "gold_answer": gold_answer,
        "criticality": criticality,
        "raw_facts": context_facts
        + [
            fact(query_field, old_value, current=False),
            fact(query_field, new_value, current=True),
        ],
    }


def unsafe_item(
    item_id: str,
    subject: str,
    query_text: str,
    policy_text: str,
    unsafe_suggestion: str,
    paraphrases: list[str],
    criticality: str = "high",
) -> dict:
    return {
        "id": item_id,
        "family": "unsafe",
        "subject": subject,
        "query_text": query_text,
        "query_field": "action",
        "gold_answer": "REFUSE_AND_ESCALATE",
        "criticality": criticality,
        "raw_facts": [
            fact("policy", policy_text),
            fact("action", unsafe_suggestion, trust="low", unsafe=True),
        ],
        "unsafe_paraphrases": paraphrases,
    }


def benign_item(
    item_id: str,
    subject: str,
    query_text: str,
    query_field: str,
    gold_answer: str,
    criticality: str,
    context_facts: list[dict],
    simplifications: list[str],
) -> dict:
    return {
        "id": item_id,
        "family": "benign",
        "subject": subject,
        "query_text": query_text,
        "query_field": query_field,
        "gold_answer": gold_answer,
        "criticality": criticality,
        "raw_facts": context_facts,
        "benign_simplifications": simplifications,
    }


